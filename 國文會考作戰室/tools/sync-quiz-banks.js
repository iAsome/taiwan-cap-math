const fs = require("node:fs");
const path = require("node:path");
const vm = require("node:vm");

const root = path.resolve(__dirname, "..");
const context = { window: {}, console };
for (const file of ["data.js", "analysis-data.js", "questions.js"]) {
  vm.runInNewContext(fs.readFileSync(path.join(root, file), "utf8"), context, { filename: file });
}

const { units } = context.window.CHINESE_DATA;
const { quizCatalog } = context.window.EXAM_ENGINE;
const outRoot = path.join(root, "quiz-banks");
fs.mkdirSync(outRoot, { recursive: true });

const familyByUnit = unit => ({
  unitId: unit.id,
  title: unit.title,
  grade: unit.grade,
  domain: unit.domain,
  bank: {
    purpose: "Question-bank drills: pre-authored variant pool for this unit, seeded random pick + shuffled choices.",
    templateSource: `questions.js / bank / unit ${unit.id}`,
    guardrails: [
      "Junior-high CAP Chinese scope only.",
      "Each unit keeps a reviewed pool of 2-3 question variants; answers/steps/tips are authored, not generated.",
      "Reading-set items keep the shared passage separate from the question stem."
    ]
  }
});

for (const quiz of quizCatalog) {
  const dir = path.join(outRoot, quiz.id);
  fs.mkdirSync(dir, { recursive: true });
  const bank = {
    id: quiz.id,
    title: quiz.title,
    grade: quiz.grade,
    term: quiz.term,
    chapter: quiz.chapter || null,
    scope: quiz.scope,
    officialCodes: quiz.officialCodes,
    runtime: {
      generator: "questions.js",
      function: "window.EXAM_ENGINE.generateQuiz",
      questionCount: quiz.questionCount,
      minutes: quiz.minutes,
      nonRepeatPolicy: "app.js stores recent quiz signatures in localStorage and re-rolls seed when a duplicate signature appears."
    },
    units: quiz.unitIds.map(id => familyByUnit(units.find(unit => unit.id === id))).filter(Boolean)
  };
  fs.writeFileSync(path.join(dir, "question-bank.json"), `${JSON.stringify(bank, null, 2)}\n`, "utf8");
  fs.writeFileSync(path.join(dir, "README.md"), `# ${quiz.title}

- Quiz ID: ${quiz.id}
- Units: ${quiz.unitIds.map(id => units.find(unit => unit.id === id)?.title).filter(Boolean).join(" / ")}
- Questions: ${quiz.questionCount}
- Minutes: ${quiz.minutes}
- Official codes: ${quiz.officialCodes}

This folder is the maintenance home for this quiz bank. Static metadata is kept here; live question variants and answers are generated by \`questions.js\` from its authored bank pool, so the actual text is never duplicated here.
`, "utf8");
}

console.log(`Synced ${quizCatalog.length} quiz bank folders.`);
