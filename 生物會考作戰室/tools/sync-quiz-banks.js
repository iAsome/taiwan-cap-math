const fs = require("node:fs");
const path = require("node:path");
const vm = require("node:vm");

const root = path.resolve(__dirname, "..");
const context = { window: {}, console };
for (const file of ["data.js", "analysis-data.js", "quiz-taxonomy.js", "questions.js"]) {
  vm.runInNewContext(fs.readFileSync(path.join(root, file), "utf8"), context, { filename: file });
}

const { units } = context.window.BIO_DATA;
const taxonomy = context.window.QUIZ_TAXONOMY || {};
const { quizCatalog } = context.window.EXAM_ENGINE;
const outRoot = path.join(root, "quiz-banks");
fs.mkdirSync(outRoot, { recursive: true });

const familyByUnit = unit => ({
  unitId: unit.id,
  title: unit.title,
  grade: unit.grade,
  domain: unit.domain,
  basic: {
    purpose: "Drills confirm definitions, mechanisms, and standard reasoning steps; all questions are multiple choice.",
    difficultyRange: [1, 3],
    templateSource: `questions.js / makeQuizUnitQuestion / unit ${unit.id}`,
    guardrails: [
      "Junior-high natural science (biology) scope only.",
      "Scenarios and numbers are generated by questions.js; answers are computed from the same parameters.",
      "No constructed-response items: the official 自然 exam is multiple choice only."
    ]
  }
});

for (const quiz of quizCatalog) {
  const dir = path.join(outRoot, quiz.id);
  fs.mkdirSync(dir, { recursive: true });
  const bank = {
    id: quiz.id,
    title: quiz.title,
    grade: quiz.grade,
    term: quiz.term || null,
    chapter: quiz.chapter || null,
    scope: quiz.scope,
    officialCodes: quiz.domainNote,
    runtime: {
      generator: "questions.js",
      function: "window.EXAM_ENGINE.generateQuiz",
      questionCount: quiz.questionCount,
      minutes: quiz.minutes,
      nonRepeatPolicy: "app.js stores recent quiz signatures in localStorage and re-rolls seed when a duplicate signature appears.",
      taxonomySource: taxonomy[quiz.id] ? "quiz-taxonomy.js supplies fine-grained topic seeds for this quiz." : "No fine-grained taxonomy yet; falls back to unit-level generators."
    },
    taxonomy: taxonomy[quiz.id]?.sections || [],
    units: quiz.unitIds.map(id => familyByUnit(units.find(unit => unit.id === id))).filter(Boolean)
  };
  fs.writeFileSync(path.join(dir, "question-bank.json"), `${JSON.stringify(bank, null, 2)}\n`, "utf8");
  fs.writeFileSync(path.join(dir, "README.md"), `# ${quiz.title}

- Quiz ID: ${quiz.id}
- Units: ${quiz.unitIds.map(id => units.find(unit => unit.id === id)?.title).filter(Boolean).join(" / ")}
- Questions: ${quiz.questionCount}
- Minutes: ${quiz.minutes}
- Domain / topic: ${quiz.domainNote}

This folder is the maintenance home for this quiz bank. Static metadata is kept here; live numeric variants and answers are generated by \`questions.js\`, so answers are calculated from the same random parameters instead of being hand-copied. All questions are multiple choice, matching the 自然科 exam format.
`, "utf8");
}

console.log(`Synced ${quizCatalog.length} quiz bank folders.`);
