// One-off generator: derives quiz-banks/<id>/README.md + question-bank.json metadata
// from earth-data.js / quiz-taxonomy.js / questions.js, mirroring taiwan-cap-math's
// quiz-banks folders. Run once with `node tools-gen-quiz-banks.mjs`; safe to re-run.
import fs from "node:fs";
import vm from "node:vm";

const context = { window: {}, console };
vm.createContext(context);
for (const file of ["earth-data.js", "quiz-taxonomy.js", "questions.js"]) {
  vm.runInContext(fs.readFileSync(file, "utf8"), context, { filename: file });
}
const { quizCatalog, groupNames } = context.window.EXAM_ENGINE;
const taxonomy = context.window.QUIZ_TAXONOMY;

fs.mkdirSync("quiz-banks", { recursive: true });
for (const item of quizCatalog) {
  const dir = `quiz-banks/${item.id}`;
  fs.mkdirSync(dir, { recursive: true });
  const taxo = item.unitIds.map(id => taxonomy[`u${id}`]).filter(Boolean);
  const questionCount = taxo.reduce((sum, t) => sum + t.sections.reduce((s, sec) => s + sec.topics.length, 0), 0);
  const readme = `# ${item.title}

- Quiz ID: ${item.id}
- Scope: ${item.scope === "review" ? `領域總複習｜${groupNames[item.group]}` : "單元小考"}
- Units: ${item.unitIds.join(", ")}
- Questions: ${questionCount}
- Minutes: ${item.minutes}
- Source: ${item.source}

This folder is the maintenance home for this quiz bank. Static metadata is kept here; live question variants and answers are generated by \`questions.js\` calling into \`quiz-taxonomy.js\`, so answers are calculated from the same source instead of being hand-copied.
`;
  fs.writeFileSync(`${dir}/README.md`, readme);
  const bank = {
    id: item.id, title: item.title, group: item.group, scope: item.scope,
    officialCodes: item.officialCodes,
    runtime: {
      generator: "questions.js + quiz-taxonomy.js",
      function: "window.EXAM_ENGINE.generateQuiz",
      questionCount, minutes: item.minutes,
      nonRepeatPolicy: "app.js stores recent quiz signatures in localStorage and re-rolls seed when a duplicate signature appears."
    },
    taxonomy: taxo.flatMap(t => t.sections.map(sec => ({ title: sec.title, topics: sec.topics.map(topic => ({ id: topic.id, title: topic.title })) })))
  };
  fs.writeFileSync(`${dir}/question-bank.json`, JSON.stringify(bank, null, 2));
}
console.log("generated", quizCatalog.length, "quiz-bank folders");
