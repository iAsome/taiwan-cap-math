// 自動產生 quiz-banks/<id>/README.md + question-bank.json，來源為 history-data.js + quiz-taxonomy.js + questions.js 的 quizCatalog。
// 用法：node tools/sync-quiz-banks.js（在 歷史會考作戰室/ 目錄下執行）；可重複執行。
const fs = require("node:fs");
const path = require("node:path");
const vm = require("node:vm");

const root = path.resolve(__dirname, "..");
const context = { window: {}, console, Date, Math };
vm.createContext(context);
for (const file of ["history-data.js", "quiz-taxonomy.js", "questions.js"]) {
  vm.runInContext(fs.readFileSync(path.join(root, file), "utf8"), context, { filename: file });
}
const { quizCatalog } = context.window.EXAM_ENGINE;

fs.mkdirSync(path.join(root, "quiz-banks"), { recursive: true });
for (const item of quizCatalog) {
  const dir = path.join(root, "quiz-banks", item.id);
  fs.mkdirSync(dir, { recursive: true });
  const readme = `# ${item.title}

- Quiz ID: ${item.id}
- Scope: ${item.scope === "review" ? "領域總複習" : "單元小考"}
- Units: ${item.unitIds.join(", ")}
- Questions: ${item.questionCount}
- Minutes: ${item.minutes}
- Official codes: ${item.officialCodes || ""}
${item.source ? `- Source: ${item.source}\n` : ""}
This folder is the maintenance home for this quiz bank. Static metadata is kept here; live question variants and answers are generated by \`questions.js\` at runtime, so answers are calculated from the same source instead of being hand-copied.
`;
  fs.writeFileSync(path.join(dir, "README.md"), readme);
  const bank = {
    id: item.id, title: item.title, group: item.group, chapter: item.chapter || null,
    scope: item.scope, officialCodes: item.officialCodes || null,
    runtime: {
      generator: "questions.js",
      function: "window.EXAM_ENGINE.generateQuiz",
      questionCount: item.questionCount, minutes: item.minutes,
      nonRepeatPolicy: "app.js stores recent quiz signatures in localStorage and re-rolls seed when a duplicate signature appears."
    },
    unitIds: item.unitIds
  };
  fs.writeFileSync(path.join(dir, "question-bank.json"), JSON.stringify(bank, null, 2));
}
console.log("generated", quizCatalog.length, "quiz-bank folders");
