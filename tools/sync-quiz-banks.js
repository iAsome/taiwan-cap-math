const fs = require("node:fs");
const path = require("node:path");
const vm = require("node:vm");

const root = path.resolve(__dirname, "..");
const context = { window: {}, console };
for (const file of ["data.js", "analysis-data.js", "questions.js"]) {
  vm.runInNewContext(fs.readFileSync(path.join(root, file), "utf8"), context, { filename: file });
}

const { units } = context.window.MATH_DATA;
const { quizCatalog, allowsAdvanced } = context.window.EXAM_ENGINE;
const outRoot = path.join(root, "quiz-banks");
fs.mkdirSync(outRoot, { recursive: true });

const familyByUnit = unit => ({
  unitId: unit.id,
  title: unit.title,
  grade: unit.grade,
  domain: unit.domain,
  basic: {
    purpose: "Basic drills: confirm rules, definitions, and standard procedures.",
    difficultyRange: [1, 2],
    templateSource: `questions.js / makeQuizUnitQuestion / unit ${unit.id}`,
    guardrails: [
      "Junior-high scope only.",
      "Numbers are generated by questions.js and answers are calculated from the same parameters.",
      "Prefer familiar one-step or two-step rule practice."
    ]
  },
  advanced: allowsAdvanced(unit.id) ? {
    purpose: "Advanced drills: flexible CAP-style use of the same unit ideas.",
    difficultyRange: [3, 5],
    templateSource: `questions.js / makeQuizUnitQuestion / unit ${unit.id}`,
    guardrails: [
      "Stay inside junior-high mathematics.",
      "May combine concepts, but should not require senior-high formulas.",
      "Solutions must remain explainable by the handbook formulas and unit concepts."
    ]
  } : null
});

for (const quiz of quizCatalog) {
  const dir = path.join(outRoot, quiz.id);
  fs.mkdirSync(dir, { recursive: true });
  const bank = {
    id: quiz.id,
    title: quiz.title,
    grade: quiz.grade,
    term: quiz.term,
    chapter: quiz.chapter || null,
    scope: quiz.scope,
    officialCodes: quiz.officialCodes,
    runtime: {
      generator: "questions.js",
      function: "window.EXAM_ENGINE.generateQuiz",
      questionCount: quiz.questionCount,
      minutes: quiz.minutes,
      nonRepeatPolicy: "app.js stores recent quiz signatures in localStorage and re-rolls seed when a duplicate signature appears.",
      levelSplit: "Advanced questions appear only for units that need flexible application; chapter quizzes keep them to about one third at most."
    },
    units: quiz.unitIds.map(id => familyByUnit(units.find(unit => unit.id === id))).filter(Boolean)
  };
  fs.writeFileSync(path.join(dir, "question-bank.json"), `${JSON.stringify(bank, null, 2)}\n`, "utf8");
  fs.writeFileSync(path.join(dir, "README.md"), `# ${quiz.title}

- Quiz ID: ${quiz.id}
- Units: ${quiz.unitIds.map(id => units.find(unit => unit.id === id)?.title).filter(Boolean).join(" / ")}
- Questions: ${quiz.questionCount}
- Minutes: ${quiz.minutes}
- Official codes: ${quiz.officialCodes}

This folder is the maintenance home for this quiz bank. Static metadata is kept here; live numeric variants and answers are generated by \`questions.js\`, so answers are calculated from the same random parameters instead of being hand-copied.
`, "utf8");
}

console.log(`Synced ${quizCatalog.length} quiz bank folders.`);
